import json
from datetime import datetime
from pathlib import Path
import pandas as pd

import prompt_templates
from omegaconf import OmegaConf
from tqdm import tqdm
import re


def make_results_dir(config, benchmark_path):
    # make a directory for the run inside results_dir
    # inside the run directory make one dir per benchmark

    run_name = "-".join(
        (datetime.now().replace(microsecond=0).isoformat(), config.run_readable_name)
    )

    run_path = (
        Path(config.benchmarks.results_dir) / Path(benchmark_path).stem / run_name
    )

    run_path.mkdir(parents=True, exist_ok=False)

    # save a copy of the current config file in the run directory so it is easy to rerun later
    OmegaConf.save(config, run_path / "config.yml")

    return run_path


def outputs_to_dict(problems, outputs):
    # given an outputs object, like the one generated by llm.generate,
    # turn it into a dictionary ready to be saved to json
    # outputs is a list, one item per problem in the benchmark

    output_list = []

    for i in range(len(problems)):

        output_dict = {}

        output_dict["problem"] = problems[i]
        output_dict["generated_text"] = [_.text for _ in outputs[i].outputs]
        output_dict["finish_reason"] = [_.finish_reason for _ in outputs[i].outputs]

        output_list.append(output_dict)

    return output_list


def save_results(run_path, benchmark_path, problems, outputs):

    output_dicts = outputs_to_dict(problems, outputs)

    with open(run_path / (Path(benchmark_path).stem + "_output.jsonl"), "w") as f:
        for output_dict in output_dicts:
            f.write(json.dumps(output_dict) + "\n")


def load_problems(benchmark_path, config):
    # Load all JSONL files from the given path, concatenate all of them and
    # return a single list of dictionaries.

    jsonl_files = benchmark_path.glob("*.jsonl")

    problems = []
    for jsonl_file in jsonl_files:
        with open(jsonl_file, "r") as f:
            for line in f.readlines():
                problems.append(json.loads(line))

    return problems[: min(config.benchmarks.max_questions, len(problems))]


def get_template(template_style):

    if template_style in ["grpo", "sft"]:
        template = prompt_templates.TEMPLATE
    elif template_style == "grpo_think":
        template = prompt_templates.TEMPLATE_THINK
    else:
        raise ValueError(f"Invalid template style: {template_style}")

    return template


def make_prompts_from_template(problems, config):

    # problems is a list of dicts, one dict per problem
    # we expect them to have at least the 'question' and 'options' keys

    messages = []

    print(f"Generating prompts using template style {config.prompt.template_style}:")

    for problem in tqdm(problems):

        # FIXME we can probably modify the benchmark to have the visit summary as part of the question
        if "visit_summary" in problem:
            question = f'{problem["visit_summary"]}\n\n{problem["question"]}'
        else:
            question = problem["question"]

        # Format the prompt
        prompt = get_template(config.prompt.template_style).format(
            question=question, options=problem["options"]
        )

        # Add system prompt if config requests it
        if config.prompt.system_prompt:
            message = [
                {"role": "system", "content": config.prompt.system_prompt},
                {"role": "user", "content": prompt},
            ]
        else:
            message = [{"role": "user", "content": prompt}]

        messages.append(message)

    return messages


def run_benchmark(llm, benchmark_path, config):

    problems = load_problems(Path(benchmark_path), config)

    messages = make_prompts_from_template(problems, config)

    outputs = llm.generate(messages)

    return problems, outputs


def load_results(file_path):
    # Load JSONL files from the given path returns a pandas dataframe.
    # The dataframe is in tall format, with one row per output generated.
    # If the benchmark was run with LLM.n=5 and 100 questions, it will have
    # 500 rows.

    results = []
    with open(file_path) as f:
        for line in f:
            results.append(json.loads(line))

    results_df = (
        pd.json_normalize(results)
        .explode(["generated_text", "finish_reason"])
        .rename(columns=lambda x: x.split(".")[-1]) # dangerous, does not check for duplicate columns
    )

    return results_df


